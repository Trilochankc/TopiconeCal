package com.trilochan.topiconecal;

public class CalcClass {
    private int one;
    private int two;

    public int getOne() {
        return one;
    }

    public void setOne(int one) {
        this.one = one;
    }

    public int getTwo() {
        return two;
    }

    public void setTwo(int two) {
        this.two = two;
    }
    public int add()
    {
        return one+two;
    }

    public int Subtract()
    {
        return one-two;
    }

    public  int Multiply()
    {
        return one*two;
    }
}
